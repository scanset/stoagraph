// Command stag-proxy is the standing gating MCP server — the front door an agent host
// connects to (Planning/24). It is an MCP SERVER to the agent and an MCP CLIENT to the
// real downstream server, with the deterministic gate in the middle: every tool call is
// gated, cleared calls forward to the downstream, denied ones return a gate error and
// never reach it. It holds NO model and NO keys — only the gate, the routes, and the
// audit log. Config (routes + downstream) comes from the same store the console manages.
//
// v1: single downstream, served over stdio (the agent spawns this and speaks MCP over its
// stdio). Routes are resolved once at startup. Streamable HTTP + session->recipe is next.
package main

// file-kw: cmd gate mcp gating proxy daemon session-to-recipe stdio streamable-http live-vs-ready fail-closed dispatch-role

